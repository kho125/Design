
// 대학별 발표 순서
function presOrderContents() {
    let order = document.getElementById("order");
    order.style.display = (order.style.display === "none") 
    ? "block" 
    : "none";
}

document.addEventListener("DOMContentLoaded", function() {
    order.style.display = (order.style.display === "none") 
    ? "block" 
    : "none";
});